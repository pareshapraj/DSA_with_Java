public class simpleInterest{
	public static void main(String args[]){
		float principal,time,rate;

		Scanner sc=new Scanner(System.in);
		principal=sc.nextFloat();
		time=sc.nextFloat();
		rate=sc.nextFloat();

		float si=(principal*time*rate)/100;
		System.out.println(si);
		sc.close();
	}
}

























